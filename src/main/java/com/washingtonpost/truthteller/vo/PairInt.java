public class PairInt {
    private final int p1;
    private final int p2;
    
    public PairInt(int p1, int p2){
        this.p1=p1;
        this.p2=p2;
    }

    public int getP1() {
        return p1;
    }

    public int getP2() {
        return p2;
    }   
    
}
